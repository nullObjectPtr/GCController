    public SymbolInfoCollection(SFSymbolInfo[] symbols) 
    { 
        Symbols = symbols; 
    } 
     
    public bool TryGetFilledVariant(string symbolName, out string filledVariantName) 
    { 
        LazyInitBimap(); 
         
        if(unfilled.TryGetValue(symbolName, out var symbolInfo)) 
        { 
            if (symbolInfo.hasFill) 
            { 
                filledVariantName = symbolInfo.filledSymbolName; 
                return true; 
            } 
        } 
 
        filledVariantName = ""; 
        return false; 
    } 
 
    public IEnumerable<string> AllSymbolNames() 
    { 
        return Symbols 
            .Select(s => s.symbolName) 
            .Concat(Symbols.Where(s => s.hasFill).Select(s => s.filledSymbolName)); 
    } 
 
    private void LazyInitBimap() 
    { 
        if (filled != null) return; 
         
        filled = new Dictionary<string, SFSymbolInfo>(); 
        unfilled = new Dictionary<string, SFSymbolInfo>(); 
             
        foreach (var symbolInfo in Symbols) 
        { 
            unfilled[symbolInfo.symbolName] = symbolInfo; 
            if (symbolInfo.hasFill) 
                filled[symbolInfo.filledSymbolName] = symbolInfo; 
        } 
    } 
